#include <tinyhal.h>


GPIO_PIN VirtualKey_GetPins(UINT32 virtualKey)
{
    GPIO_PIN pin;

    switch (virtualKey)
    {
    case VK_MENU:
        pin = GPIO_PIN_NONE;
        break;
    case VK_SELECT:
        pin = GPIO_PIN_NONE;
        break;
    case VK_LEFT:
        pin = AT91_GPIO_Driver::PB0;
        break;
    case VK_RIGHT:
        pin = AT91_GPIO_Driver::PB1;
        break;
    default:
        pin = GPIO_PIN_NONE;
        break;
    }
   
   return pin;
}

