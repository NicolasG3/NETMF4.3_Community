#include <HardwareSerial_decl.h>

//Get the hardware serial number as byte[16] array
BOOL HWSN_GetSerial(UINT8 data[]) {
	//hardware serial real size is 12 bytes
	//right packed
	memcpy((void*)&data[4], (const void*)0x1FFF7A10, 12);
	return TRUE;
}
